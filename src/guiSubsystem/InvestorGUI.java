    /**
     * Error checking is performed when the Submit Button is selected
     * to make sure that when BrokerageControl is called that all of the
     * information is correct.  If invalid information is given then error
     * window will appear and the Place Order screen will be brought back.
     * 
     * @param evt User selects Submit Button on Place Order screen.
     */
    
    private void Submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Submit_ButtonActionPerformed
        
        double amt = 0;
        double lmtprc = 0;
        String symbol = "";
        boolean validInput = true;
        try {
            amt = Double.valueOf(Number_of_Stock_TextField.getText()).doubleValue();
        }
        catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(Place_order_panel, "Stock Amount"
                    + " must be a numerical amount.", "Invalid Amount",
                    JOptionPane.WARNING_MESSAGE);
            validInput = false;
        }
        if(Limit_Button.isSelected())
        {
            try {
                lmtprc = Double.valueOf(Limit_Price_Textfield.getText()).doubleValue();
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(Place_order_panel, "Limit Price"
                        + " must be a numerical amount.", "Invalid Limit Price",
                        JOptionPane.WARNING_MESSAGE);
                validInput = false;
            }
        }
        
        if(validInput) 
        {
            String Symbol;
            int amount;
            Double limitprice = 0.0;
            Boolean buyOrder,limitOrder,brokerAssist;
            
            Symbol = Stock_Symbol_TextField.getText().toUpperCase();
            amount = Integer.parseInt(Number_of_Stock_TextField.getText());
            
            if(!Limit_Price_Textfield.getText().equals("")) {
                limitprice = Double.parseDouble(Limit_Price_Textfield.getText());
            }
            
            buyOrder = Buy_Button.isSelected();
            limitOrder = Limit_Button.isSelected();
            brokerAssist = Yes_Broker_Assistance_Button.isSelected();
            BrokerageControl.submitOrder(Symbol,amount,limitprice,buyOrder,limitOrder,brokerAssist);
            Number_of_Stock_TextField.setText("");
            Stock_Symbol_TextField.setText("");
            Limit_Price_Textfield.setText("");
            Buy_Button.setSelected(true);
            Sell_Button.setSelected(false);
            Yes_Broker_Assistance_Button.setSelected(false);
            No_Broker_Assistance_Button.setSelected(true);
            Market_Button.setSelected(true);
            Limit_Button.setSelected(false);
            JOptionPane.showMessageDialog(Place_order_panel,
                        "Order Successfully Submitted.", "Submitted",
                        JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Submit_ButtonActionPerformed

