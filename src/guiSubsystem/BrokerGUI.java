    /**
     * This method checks for error in user input and that the new Investor
     * does not already exist.  If all error checks are passed then 
     * BrokerageControl is called to handle the creation.
     * 
     * @param evt User clicking the ok button on the New Investor screen. 
     */
    
    private void New_Investor_ok_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_New_Investor_ok_buttonActionPerformed
        String fname = FirstnameText.getText();
        String lname = LastnameText.getText();
        String uid = IDText.getText();
        char[] pword = DefaultPasswordText.getPassword();
        Broker broker = (Broker)BrokerageControl.getUserList().get(BrokerageControl.getUserIndex());
        String dep = InitialDepositText.getText();
        
        long userid = 0;
        double deposit = 0;
        
        boolean validInput = true;
        try {
            userid = Long.valueOf(uid).longValue();
            deposit = Double.valueOf(dep).doubleValue();
        }
        catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(New_Investor_Account_panel, "User ID and"
                    + " deposit must be numerical values.", "Invalid Input",
                    JOptionPane.WARNING_MESSAGE);
            validInput = false;
        }
        
        boolean validID = false;
        if(validInput) {
            if(BrokerageControl.isUniqueID(userid)) {
                validID = true;
            }
            else {
                JOptionPane.showMessageDialog(New_Investor_Account_panel, "The user ID"
                        + " you entered has alraedy been used.", "Invalid User"
                        + " ID", JOptionPane.WARNING_MESSAGE);
            }
        }
        
        if(validInput && validID) {
            BrokerageControl.newInvestor(userid, fname, lname, pword, broker,
                    deposit);
        }
        
        New_Investor_Created_Frame.setSize(300,200);
        New_Investor_Created_Frame.setVisible(true);
    }//GEN-LAST:event_New_Investor_ok_buttonActionPerformed

